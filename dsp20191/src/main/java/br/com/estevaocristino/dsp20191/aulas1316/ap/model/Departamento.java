package br.com.estevaocristino.dsp20191.aulas1316.ap.model;

public class Departamento {

    private Long id;

    private String nome;

    public Departamento() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
}
